document.addEventListener("DOMContentLoaded", () => {
  const input = document.getElementById("category3Search");
  const result = document.getElementById("category3Result");

  if (!input || !result) return;

  input.addEventListener("input", () => {
    const query = input.value.trim().toLowerCase();

    if (!query) {
      result.textContent = "Начни вводить текст для поиска вопроса.";
      return;
    }

    const matches = category3Answers.filter((item) => {
      const haystack = [
        item.question,
        item.explanation,
        item.hint,
        ...item.keywords
      ].join(" ").toLowerCase();

      return haystack.includes(query);
    });

    if (!matches.length) {
      result.textContent = "Ничего не найдено. Попробуй другое слово.";
      return;
    }

    result.innerHTML = matches.map((item) => `
      <div class="answer-card">
        <p class="answer-card__question">${item.number}. ${item.question}</p>
        <p class="answer-card__answer">${item.explanation}</p>
        <p class="answer-card__hint">💡 ${item.hint}</p>
      </div>
    `).join("");
  });
});